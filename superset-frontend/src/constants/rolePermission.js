export const returnPermissionKey = label => {
  switch (label) {
    // data pipeling
    case 'Create Pipeline':
      return dataPipeline.createPipeline.pageView.value;
    case 'Table Permission':
      return dataPipeline.tablePremission.pageView.value;
    case 'Workspace':
      return dataPipeline.workspace.pageView.value;
    case 'My Flows':
      return dataPipeline.myFlows.pageView.value;
    case 'My Outputs':
      return dataPipeline.myOutput.pageView.value;
    // sql lab
    case 'SQL Editor':
      return sqlLab.sqlEditor.pageView.value;
    case 'Saved Queries':
      return sqlLab.savedQueries.pageView.value;
    case 'Query History':
      return sqlLab.queryHistory.pageView.value;
    // data applcation tool
    case 'Power BI Template':
      return dataApplicationTools.powerBITemplate.pageView.value;
    case 'Charts':
      return dataApplicationTools.charts.pageView.value;
    case 'Dashboards':
      return dataApplicationTools.dashboard.pageView.value;
    case 'DataRobot Model List':
      return dataApplicationTools.dataRobotModalList.pageView.value;
    // setting
    case 'User Management':
      return setting.userManagement.pageView.value;
    case 'Role Management':
      return setting.roleManagement.pageView.value;
    case 'Data Domain Management':
      return setting.dataDomainManagement.pageView.value;
    case 'Dataset Management':
      return setting.datasetMangement.pageView.value;
    case 'Group Management':
      return setting.groupManagement.pageView.value;
    case 'Group Permission':
      return setting.groupPermission.pageView.value;
    default:
      return '';
  }
};
